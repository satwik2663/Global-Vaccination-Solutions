    public void popData() {

        DefaultTableModel model = (DefaultTableModel) inventoryJTable.getModel();

        model.setRowCount(0);

        for (Vaccine vaccine : enterprise.getVaccineDirectory().getVaccineList()) {
            Object row[] = new Object[3];
            row[0] = vaccine.getId();
            row[1] = vaccine;
            row[2] = vaccine.getQuantity();
            
            model.addRow(row);
        }
    }

