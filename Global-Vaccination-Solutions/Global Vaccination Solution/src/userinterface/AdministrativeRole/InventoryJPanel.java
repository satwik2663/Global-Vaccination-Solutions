    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed

        
        String vaccine = vaccineJTextField.getText();
        Integer quantity = Integer.parseInt(quantityJTextField.getText());
if(vaccine.equals("")){
    JOptionPane.showMessageDialog(null, "Vaccine name cannot be empty");
            
}
else{
        enterprise.getVaccineDirectory().addVaccine(vaccine, quantity);

        popData();
        vaccineJTextField.setText("");
        quantityJTextField.setText("");
}
    }//GEN-LAST:event_addBtnActionPerformed

